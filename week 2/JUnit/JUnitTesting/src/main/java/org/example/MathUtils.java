package org.example;

public class MathUtils {

    // Multiply two numbers
    public int multiply(int a, int b) {
        return a * b;
    }

    // Check if number is even
    public boolean isEven(int number) {
        return number % 2 == 0;
    }

    // Return a greeting message
    public String greet(String name) {
        return "Hello, " + name;
    }
}
